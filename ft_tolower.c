#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		c += 32;
	return (c);
}

// ft_tolower converts an uppercase alphabet to a lowercase character.
// Returns UPPERCASE alphabet.